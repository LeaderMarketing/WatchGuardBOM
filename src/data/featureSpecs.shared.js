function getSpecValue(slug, key) {
  return PRODUCT_SPECS[slug]?.[key] ?? '—';
}

function buildFeatureSections(category, slug) {
  const sections = SECTION_DEFS[category] || [];

  return sections.map((section) => ({
    title: section.title,
    rows: section.rows.map((row) => ({
      label: row.label,
      key: row.key,
      value: getSpecValue(slug, row.key),
    })),
  }));
}

function buildFeatureGroups(category, slug) {
  const groups = {};

  for (const section of buildFeatureSections(category, slug)) {
    groups[section.title] = section.rows.map((row) => ({
      name: row.label,
      value: row.value,
    }));
  }

  return groups;
}

function buildFeatureRecords(category, slug) {
  return buildFeatureSections(category, slug).flatMap((section) =>
    section.rows.map((row, index) => ({
      featureCategory: section.title,
      featureName: row.label,
      featureValue: row.value,
      sortOrder: index + 1,
    })),
  );
}

